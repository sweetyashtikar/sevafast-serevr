// 7. UPDATE - Update seller status (approve/reject/deactivate)
const updateSellerStatus = async (req, res) => {
    const session = await mongoose.startSession();
    session.startTransaction();
    
    try {
        const { id } = req.params;
        const { status, reason } = req.body;

        // Validate ID
        if (!mongoose.Types.ObjectId.isValid(id)) {
            await session.abortTransaction();
            session.endSession();
            return res.status(400).json({
                success: false,
                message: 'Invalid Seller ID format'
            });
        }

        // Validate status
        const validStatuses = ['approved', 'not_approved', 'deactive', 'removed'];
        if (!status || !validStatuses.includes(status)) {
            await session.abortTransaction();
            session.endSession();
            return res.status(400).json({
                success: false,
                message: `Invalid status. Valid statuses: ${validStatuses.join(', ')}`
            });
        }

        // Get seller
        const seller = await Seller.findById(id).session(session);
        if (!seller) {
            await session.abortTransaction();
            session.endSession();
            return res.status(404).json({
                success: false,
                message: 'Seller not found'
            });
        }

        // Don't allow changing to same status
        if (seller.status === status) {
            await session.abortTransaction();
            session.endSession();
            return res.status(400).json({
                success: false,
                message: `Seller is already ${status}`
            });
        }

        // Store previous status
        const previousStatus = seller.status;

        // Update status
        seller.status = status;
        seller.status_history = seller.status_history || [];
        seller.status_history.push({
            from: previousStatus,
            to: status,
            changed_at: new Date(),
            reason: reason || 'No reason provided',
            changed_by: req.user?._id || 'system'
        });

        await seller.save({ session });

        // Send notification based on status change
        if (status === 'approved') {
            // Send approval notification
            console.log(`Seller ${seller._id} approved. Notifying user ${seller.user_id}`);

            // --- Referral Reward Logic ---
            await issueReferralReward(seller.user_id, session);
            // -----------------------------

        } else if (status === 'deactive' || status === 'removed') {
            // Send deactivation/removal notification
            console.log(`Seller ${seller._id} status changed to ${status}. Reason: ${reason}`);
        }

        await session.commitTransaction();
        session.endSession();

        res.status(200).json({
            success: true,
            message: `Seller status updated from ${previousStatus} to ${status}`,
            data: {
                seller_id: seller._id,
                store_name: seller.store_info.name,
                previous_status: previousStatus,
                new_status: seller.status,
                updated_at: seller.updatedAt
            }
        });

    } catch (error) {
        await session.abortTransaction();
        session.endSession();
        
        console.error('Error updating seller status:', error);
        res.status(500).json({
            success: false,
            message: 'Failed to update seller status'
        });
    }
};

// 8. UPDATE - Update seller rating
const updateSellerRating = async (req, res) => {
    const session = await mongoose.startSession();
    session.startTransaction();
    
    try {
        const { id } = req.params;
        const { rating, review } = req.body;

        // Validate ID
        if (!mongoose.Types.ObjectId.isValid(id)) {
            await session.abortTransaction();
            session.endSession();
            return res.status(400).json({
                success: false,
                message: 'Invalid Seller ID format'
            });
        }

        // Validate rating
        if (rating === undefined || rating < 0 || rating > 5) {
            await session.abortTransaction();
            session.endSession();
            return res.status(400).json({
                success: false,
                message: 'Rating must be between 0 and 5'
            });
        }

        // Get seller
        const seller = await Seller.findById(id).session(session);
        if (!seller) {
            await session.abortTransaction();
            session.endSession();
            return res.status(404).json({
                success: false,
                message: 'Seller not found'
            });
        }

        // Calculate new average
        const currentTotal = seller.ratings.average * seller.ratings.count;
        const newCount = seller.ratings.count + 1;
        const newAverage = (currentTotal + rating) / newCount;

        // Update ratings
        seller.ratings = {
            average: parseFloat(newAverage.toFixed(2)),
            count: newCount
        };

        // Add review to history if provided
        if (review) {
            seller.review_history = seller.review_history || [];
            seller.review_history.push({
                rating,
                review: review.trim(),
                reviewed_at: new Date(),
                reviewer_id: req.user?._id || null
            });
        }

        await seller.save({ session });

        await session.commitTransaction();
        session.endSession();

        res.status(200).json({
            success: true,
            message: 'Seller rating updated successfully',
            data: {
                seller_id: seller._id,
                store_name: seller.store_info.name,
                ratings: seller.ratings,
                previous_average: currentTotal / seller.ratings.count
            }
        });

    } catch (error) {
        await session.abortTransaction();
        session.endSession();
        
        console.error('Error updating seller rating:', error);
        res.status(500).json({
            success: false,
            message: 'Failed to update seller rating'
        });
    }
};

// 9. DELETE - Remove seller (soft delete)
const deleteSeller = async (req, res) => {
    const session = await mongoose.startSession();
    session.startTransaction();
    
    try {
        const { id } = req.params;
        const { reason, permanent = false } = req.body;

        // Validate ID
        if (!mongoose.Types.ObjectId.isValid(id)) {
            await session.abortTransaction();
            session.endSession();
            return res.status(400).json({
                success: false,
                message: 'Invalid Seller ID format'
            });
        }

        // Get seller
        const seller = await Seller.findById(id).session(session);
        if (!seller) {
            await session.abortTransaction();
            session.endSession();
            return res.status(404).json({
                success: false,
                message: 'Seller not found'
            });
        }

        if (permanent) {
            // Permanent deletion (admin only)
            await Seller.findByIdAndDelete(id, { session });
            
            await session.commitTransaction();
            session.endSession();

            res.status(200).json({
                success: true,
                message: 'Seller permanently deleted',
                data: {
                    seller_id: id,
                    store_name: seller.store_info.name,
                    deleted_at: new Date()
                }
            });
        } else {
            // Soft delete (change status to removed)
            seller.status = 'removed';
            seller.removal_info = {
                removed_at: new Date(),
                removed_by: req.user?._id || null,
                reason: reason || 'No reason provided',
                permanent: false
            };

            await seller.save({ session });

            await session.commitTransaction();
            session.endSession();

            res.status(200).json({
                success: true,
                message: 'Seller marked as removed',
                data: {
                    seller_id: seller._id,
                    store_name: seller.store_info.name,
                    status: seller.status,
                    removed_at: seller.removal_info.removed_at
                }
            });
        }

    } catch (error) {
        await session.abortTransaction();
        session.endSession();
        
        console.error('Error removing seller:', error);
        res.status(500).json({
            success: false,
            message: 'Failed to remove seller'
        });
    }
};

// 10. UTILITY - Check seller serviceability by zipcode
const checkSellerServiceability = async (req, res) => {
    try {
        const { seller_id } = req.params;
        const { zipcode } = req.query;

        if (!zipcode) {
            return res.status(400).json({
                success: false,
                message: 'Zipcode is required'
            });
        }

        // Validate seller ID
        if (!mongoose.Types.ObjectId.isValid(seller_id)) {
            return res.status(400).json({
                success: false,
                message: 'Invalid Seller ID format'
            });
        }

        // Get seller
        const seller = await Seller.findById(seller_id)
            .populate('zipcodes', 'zipcode')
            .select('status zipcodes store_info.name')
            .lean();

        if (!seller) {
            return res.status(404).json({
                success: false,
                message: 'Seller not found'
            });
        }

        // Check if seller is approved
        if (seller.status !== 'approved') {
            return res.status(200).json({
                success: true,
                message: 'Seller is not approved for operations',
                data: {
                    serviceable: false,
                    reason: 'Seller not approved',
                    seller_status: seller.status
                }
            });
        }

        // Find zipcode in seller's serviceable areas
        const zipcodeDoc = await Zipcode.findOne({ zipcode });
        if (!zipcodeDoc) {
            return res.status(200).json({
                success: true,
                message: 'Zipcode not found in system',
                data: {
                    serviceable: false,
                    reason: 'Zipcode not in system'
                }
            });
        }

        const isServiceable = seller.zipcodes.some(z => 
            z._id.toString() === zipcodeDoc._id.toString()
        );

        res.status(200).json({
            success: true,
            message: isServiceable ? 'Seller services this area' : 'Seller does not service this area',
            data: {
                serviceable: isServiceable,
                seller_name: seller.store_info.name,
                zipcode,
                serviceable_areas: seller.zipcodes.map(z => z.zipcode)
            }
        });

    } catch (error) {
        console.error('Error checking serviceability:', error);
        res.status(500).json({
            success: false,
            message: 'Failed to check serviceability'
        });
    }
};

