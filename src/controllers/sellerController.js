// 2. READ - Get all sellers with filtering
const getAllSellers = async (req, res) => {
    try {
        const {
            page = 1,
            limit = 20,
            status,
            category_id,
            zipcode,
            search,
            min_rating = 0,
            max_rating = 5,
            approved_only = 'false',
            sort_by = 'createdAt',
            sort_order = 'desc'
        } = req.query;

        // Build query
        const query = {};

        // Filter by status
        if (status) {
            query.status = status;
        }

        // Filter by approval status
        if (approved_only === 'true') {
            query.status = 'approved';
        }

        // Filter by category
        if (category_id) {
            if (!mongoose.Types.ObjectId.isValid(category_id)) {
                return res.status(400).json({
                    success: false,
                    message: 'Invalid Category ID format'
                });
            }
            query.category_ids = category_id;
        }

        // Filter by zipcode
        if (zipcode) {
            const zipcodeDoc = await Zipcode.findOne({ zipcode });
            if (zipcodeDoc) {
                query.zipcodes = zipcodeDoc._id;
            }
        }

        // Filter by rating range
        query['ratings.average'] = {
            $gte: parseFloat(min_rating),
            $lte: parseFloat(max_rating)
        };

        // Search by store name or description
        if (search) {
            query.$text = { $search: search };
        }

        // Calculate pagination
        const pageNum = parseInt(page);
        const limitNum = parseInt(limit);
        const skip = (pageNum - 1) * limitNum;

        // Get total count
        const total = await Seller.countDocuments(query);

        // Determine sort
        let sort = { [sort_by]: sort_order === 'desc' ? -1 : 1 };
        
        // If searching, sort by text score
        if (search) {
            sort = { score: { $meta: 'textScore' }, ...sort };
        }

        // Build query for population
        let sellersQuery = Seller.find(query, search ? { score: { $meta: 'textScore' } } : null)
            .populate('user_id', 'username email mobile')
            .populate('category_ids', 'name')
            .populate('zipcodes', 'zipcode')
            .sort(sort)
            .skip(skip)
            .limit(limitNum);

        const sellers = await sellersQuery.lean();

        // Get statistics
        const stats = {
            total_sellers: await Seller.countDocuments(),
            approved_sellers: await Seller.countDocuments({ status: 'approved' }),
            pending_approval: await Seller.countDocuments({ status: 'not_approved' }),
            deactivated: await Seller.countDocuments({ status: 'deactive' }),
            removed: await Seller.countDocuments({ status: 'removed' })
        };

        res.status(200).json({
            success: true,
            message: 'Sellers retrieved successfully',
            data: {
                sellers: sellers.map(seller => ({
                    ...seller,
                    is_active: seller.status === 'approved' || seller.status === 'not_approved'
                })),
                statistics: stats,
                pagination: {
                    current_page: pageNum,
                    total_pages: Math.ceil(total / limitNum),
                    total_items: total,
                    items_per_page: limitNum,
                    has_next: pageNum < Math.ceil(total / limitNum),
                    has_previous: pageNum > 1
                }
            }
        });

    } catch (error) {
        console.error('Error fetching sellers:', error);
        res.status(500).json({
            success: false,
            message: 'Failed to fetch sellers'
        });
    }
};

